def _build_reverse_index():
    index = {}
    for currency, data in TRANSFER_PARTNERS.items():
        for airline in data.get("airlines", []):
            prog = airline["program"]
            if prog not in index:
                index[prog] = []
            index[prog].append({
                "currency": currency,
                "currency_name": data["full_name"],
                "cards": data["cards"],
                "ratio": airline["ratio"],
                "bonus": airline.get("bonus"),
                "note": airline.get("note"),
            })
    return index

REVERSE_INDEX = _build_reverse_index()

def get_transfer_partners(airline_program=None, card_currency=None):
    aliases = {
        "air_canada": "aeroplan", "ac": "aeroplan",
        "ua": "united", "aa": "american", "dl": "delta",
        "b6": "jetblue", "as": "alaska", "af": "flyingblue",
        "klm": "flyingblue", "ba": "british_airways", "sq": "singapore",
    }
    if airline_program:
        prog = aliases.get(airline_program.lower(), airline_program.lower())
        matches = REVERSE_INDEX.get(prog, [])
        if not matches:
            return {
                "airline": prog,
                "can_transfer_from": [],
                "summary": f"No major card currency transfers directly to {prog}."
            }
        parts = []
        for m in matches:
            s = f"{m['currency_name']} ({m['ratio']}"
            if m.get("bonus"):
                s += f", {m['bonus']}"
            s += ")"
            parts.append(s)
        return {
            "airline": prog,
            "can_transfer_from": matches,
            "summary": f"{prog.title()} can be reached from {len(matches)} card currencies: " + "; ".join(parts) + "."
        }
    elif card_currency:
        data = TRANSFER_PARTNERS.get(card_currency.lower())
        if not data:
            return {"error": f"Unknown: {card_currency}", "valid": list(TRANSFER_PARTNERS.keys())}
        return {
            "currency": card_currency,
            "full_name": data["full_name"],
            "cards": data["cards"],
            "airlines": data["airlines"],
            "summary": f"{data['full_name']} transfers to {len(data['airlines'])} airline programs."
        }
    return {"error": "Provide airline_program or card_currency."}

